import { readFile, writeFile } from "node:fs/promises";
import { createHash } from "node:crypto";

async function main() {
  const nextThemesPath = new URL("../node_modules/next-themes/dist/index.js", import.meta.url);
  const outputPath = new URL("../lib/csp-hashes.ts", import.meta.url);

  const source = await readFile(nextThemesPath, "utf8");

  const startToken = "var I=";
  const endToken = ";var Q=";
  const startIndex = source.indexOf(startToken);
  if (startIndex === -1) {
    throw new Error("Could not find start token 'var I=' in next-themes dist file.");
  }

  const fnStart = startIndex + startToken.length;
  const endIndex = source.indexOf(endToken, fnStart);
  if (endIndex === -1) {
    throw new Error("Could not find end token ';var Q=' in next-themes dist file.");
  }

  const funcText = source.slice(fnStart, endIndex);
  // Keep in sync with props in components/theme-provider.tsx
  const params = '"class","venshield-theme","system",null,["light","dark"],null,true,true';
  const scriptText = `(${funcText})(${params})`;

  const base64 = createHash("sha256").update(scriptText, "utf8").digest("base64");
  const hash = `'sha256-${base64}'`;

  const fileContent = [
    "// AUTO-GENERATED — do not edit manually. Run: node scripts/compute-themes-hash.mjs",
    "// Regenerated by postinstall and prebuild scripts.",
    `export const NEXT_THEMES_FOUC_HASH = ${hash};`,
    "",
  ].join("\n");

  await writeFile(outputPath, fileContent, "utf8");
  console.log("Generated lib/csp-hashes.ts with NEXT_THEMES_FOUC_HASH.");
}

main().catch((error) => {
  console.error("Failed to generate next-themes CSP hash:", error);
  process.exit(1);
});
